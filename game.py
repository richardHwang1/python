캐릭터 = {
    '이름': '기사',
    '레벨': 12,
    '아이템': {'검':'불꽃검','갑옷':'풀플레이트'},
    '스킬': ['베기','세게베기','아주세게베기']
}

for 키 in 캐릭터:
    if type(캐릭터[키]) is str:
        print(키, ':', 캐릭터[키])
    elif type(캐릭터[키]) is int:
        print(키, ':', 캐릭터[키])
    elif type(캐릭터[키]) is dict:
        for 장비 in 캐릭터[키]:
            print(장비, ':', 캐릭터[키][장비])
    else:
        for e in 캐릭터[키]:
            print(키, ':', e)